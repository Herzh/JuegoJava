	@Override
	public void keyPressed(KeyEvent e) {

		int key = e.getKeyCode();

		// Mover a la Izquierda
		if (key == 37 && x > 0) {
			x -= 10;
			nave1.setLocation(x, 506);
		}
		
		// Mover a la derecha
		if (key == 39 && x < 600 - 61) {
			x += 10;
			nave1.setLocation(x, 506);
		}

		if (key == 32) {
			disparar();
		}
	}

