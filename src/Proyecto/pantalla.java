	public void disparar() {
		Bala bala = new Bala(this, this.nave1.getLocation().x);
		this.add(bala);
		this.repaint();
		balas.add(bala);
	}

	public void desaparecerBala(Bala bala) {
		this.remove(bala);
		this.repaint();
	}

